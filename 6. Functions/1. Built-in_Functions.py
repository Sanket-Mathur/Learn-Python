"""
Built in functions are those functions that are predefined by the python interpreter.
In the previous files you must have already some such functions.
Here are a few more examples of such functions
abs() - returns the magnitude of the value i.e. the positive value
bool() - returns the bool value associated with a number i.e. False for 0 and true for others
eval() - evaluates a string having some command, you can also use exec()
"""

# Absolute function
print(abs(34))
print(abs(-54))

# Bool function
print(bool(0))
print(bool(16))

# Eval function
s = 'print("ABCD")'
eval(s)


